class Numeric #LA CLASE NUMERIC SE HA EXPANDIDO EXITOSAMENTE
    def sumar(x)
        self+(x)
    end
    def restar(x)
        self-(x)
    end
    def multi(x)
        self*(x)
    end
    def divi(x)
        self/(x)
    end
end

